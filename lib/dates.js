export function getDaysInMonth(month, year) {
  return new Date(year, month, 0).getDate();
}

export function formatDate(year, month, day) {
  const mm = String(month).padStart(2, '0');
  const dd = String(day).padStart(2, '0');
  return `${year}-${mm}-${dd}`;
}
